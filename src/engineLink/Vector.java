package engineLink;

public class Vector {

	public float x;
	public float y;
	public float z;
	
	
	/**
	 * Three dimensional vector class to help moving vectors around
	 * @param xx
	 * @param yy
	 * @param zz
	 */
	public Vector(float xx, float yy, float zz){
		this.x = xx;
		this.y = yy;
		this.z = zz;
	}

}
